	<h1>¡Descalificado!</h1>
	<p>¡Te han descalificado!</p>
	<p>Como has podido comprobar tienes un tiempo limitado para comenzar la ronda y pasado ese tiempo tu cobarte adversario lo ha utilizado para librarse de la humillación.</p>
	<p>Entiende que tu adversario no sabe si estás mirando tus estadísticas, si estás pensando en donde atizarle o si te has marchado por cualquier razón.</p>
	<p>En cualquier caso, tu adversario ha visto la oportunidad y ha pedido a los jueces que te descalifiquen por tardón.</p>
